CREATE TABLE `nmt_fleet_manager`.`vehicles` (
  `id` bigint(0) UNSIGNED NOT NULL AUTO_INCREMENT,
  `manufacturer` varchar(64) NOT NULL DEFAULT "unknown",
  `model` varchar(128) NOT NULL,
  `year` smallint(4) UNSIGNED ZEROFILL NOT NULL DEFAULT 2000,
  `registration` varchar(16) NOT NULL,
  `tank_capacity` float UNSIGNED NOT NULL,
  `service_limit` smallint(4) NOT NULL DEFAULT 10000,
  `created_at` datetime(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0),
  `updated_at` datetime(0) DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP(0),
  PRIMARY KEY (`id`)
);

CREATE TABLE `nmt_fleet_manager`.`services` (
  `id` bigint(0) UNSIGNED NOT NULL AUTO_INCREMENT,
  `vehicle_id` bigint(0) UNSIGNED NOT NULL DEFAULT 0,
  `odometer` bigint(0) UNSIGNED NOT NULL DEFAULT 0,
  `cost` DECIMAL(13,2) UNSIGNED NOT NULL DEFAULT 0.0,
  `date` datetime(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0), 
  `created_at` datetime(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0),
  `updated_at` datetime(0) DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP(0),
  PRIMARY KEY (`id`),
  FOREIGN KEY (`vehicle_id`) REFERENCES `vehicles`(`id`) ON DELETE CASCADE
);

CREATE TABLE `nmt_fleet_manager`.`journeys` (
  `id` bigint(0) UNSIGNED NOT NULL AUTO_INCREMENT,
  `vehicle_id` bigint(0) UNSIGNED NOT NULL DEFAULT 0,
  `distance` bigint(0) UNSIGNED NOT NULL DEFAULT 0,
  `date` datetime(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0), 
  `created_at` datetime(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0),
  `updated_at` datetime(0) DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP(0),
  PRIMARY KEY (`id`),
  FOREIGN KEY (`vehicle_id`) REFERENCES `vehicles`(`id`) ON DELETE CASCADE
);

CREATE TABLE `nmt_fleet_manager`.`fuel_purchases` (
  `id` bigint(0) UNSIGNED NOT NULL AUTO_INCREMENT,
  `vehicle_id` bigint(0) UNSIGNED NOT NULL DEFAULT 0,
  `litres` float UNSIGNED NOT NULL,
  `cost` DECIMAL(13,2) UNSIGNED NOT NULL DEFAULT 0.0,
  `date` datetime(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0), 
  `created_at` datetime(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0),
  `updated_at` datetime(0) DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP(0),
  PRIMARY KEY (`id`),
  FOREIGN KEY (`vehicle_id`) REFERENCES `vehicles`(`id`) ON DELETE CASCADE
);

